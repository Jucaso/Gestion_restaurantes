    public void generarGrafico1(){
        DefaultPieDataset datos = new DefaultPieDataset();
        for (int i = 0; i < Reportes.reporte1.size(); i++) {
            datos.setValue(((tablaReporte1)Reportes.reporte1.elementAt(i)).verNombreProducto(),
                          ((tablaReporte1)Reportes.reporte1.elementAt(i)).verTotal());
        }
        
        JFreeChart grafico = ChartFactory.createPieChart("Gr치fico de productos vendidos entre las fechas\n "
                + Reportes.fechaI + " hasta " + Reportes.fechaF , datos, true, true, false);
        
        ChartPanel panel = new ChartPanel(grafico);
        panel.setMouseWheelEnabled(true);
        panel.setPreferredSize(new Dimension(600,400));
        
        jPanel1.setLayout(new BorderLayout());
        jPanel1.add(panel, BorderLayout.NORTH);
        
        pack();
        repaint();
    }
    
