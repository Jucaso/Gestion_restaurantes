    private void bisiesto(java.awt.event.ItemEvent evt) {//GEN-FIRST:event_bisiesto
        int año = Integer.parseInt(añoBox.getSelectedItem()+"");
        int mes = Integer.parseInt(mesBox.getSelectedItem()+"");

        diaBox.removeAllItems();
        if(((año % 4 == 0 && año % 100 != 0) || (año % 400 == 0)) &&  mes == 02){
            for (int i = 0; i < 29; i++) {               
                diaBox.addItem(Integer.toString(i+1));
            }
        }
        else if(mes == 02){
            for (int i = 0; i < 28; i++) {               
                diaBox.addItem(Integer.toString(i+1));
            }
        }
        else if(mes == 4 || mes == 6 || mes == 9 || mes == 11){
            for (int i = 0; i < 30; i++) {               
                diaBox.addItem(Integer.toString(i+1));
            }
        }
        else{
            for (int i = 0; i < 31; i++) {               
                diaBox.addItem(Integer.toString(i+1));
            }
        }
    }//GEN-LAST:event_bisiesto
    
