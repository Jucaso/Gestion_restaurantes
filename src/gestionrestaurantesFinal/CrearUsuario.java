    public void setUsuariosVector(){
        try {
             GestionRestaurantes.s  = GestionRestaurantes.conn.createStatement();
             GestionRestaurantes.rs = GestionRestaurantes.s.executeQuery("SELECT id FROM empleado;");
             //GestionRestaurantes.s.close();
        } catch (Exception e) {
            System.out.println("Error bro... " + e);
        }

        try {
            while(GestionRestaurantes.rs.next()){
                usuarios.add(GestionRestaurantes.rs.getInt(1));
            }
        } catch (Exception e) {
            System.out.println("Error bro... " + e);
        }
    }
    public int verificaUsuario(int codigo){
        int aux = 0;
        for (int i = 0; i < usuarios.size(); i++) {
            if((int)usuarios.elementAt(i) == codigo){
                aux = 1;
                break;
            }
        }
        if(aux == 1){
            //Usuario existe
            return 1;
        }
        else{
            //Usuario no existe
            return 0;
        }
    }
    
